package components;

/**
 * 
 * 각 플레이어의 점수를 기록하고 그에 관련한 method 를 모아놓은 클래스입니다.
 *
 */
public class PlayerComponent {
	private int score;
	
	public PlayerComponent() {
		score = 0;
		
		
	}
	
	public int score() {
		return this.score();
	}
	
	public void setScore(int score) {
		this.score = score;
	}
}
